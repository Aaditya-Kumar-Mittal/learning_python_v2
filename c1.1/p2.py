# Arithmetic Operators in Python

# Addition
a = 10
b = 5
print("Addition:", a + b)  # Output: 15

# Subtraction
print("Subtraction:", a - b)  # Output: 5

# Multiplication
print("Multiplication:", a * b)  # Output: 50

# Division
print("Division:", a / b)  # Output: 2.0

# Floor Division
print("Floor Division:", a // b)  # Output: 2

# Modulus (remainder of division)
print("Modulus:", a % b)  # Output: 0

# Exponentiation
print("Exponentiation:", a ** b)  # Output: 100000

# Comparison Operators in Python

print("Equal:", a == b)        # Output: False
print("Not Equal:", a != b)    # Output: True
print("Greater Than:", a > b)  # Output: True
print("Less Than:", a < b)     # Output: False
print("Greater Than or Equal To:", a >= b)  # Output: True
print("Less Than or Equal To:", a <= b)     # Output: False

# Logical Operators in Python

print("Logical AND:", a > 0 and b > 0)  # Output: True
print("Logical OR:", a > 0 or b < 0)    # Output: True
print("Logical NOT:", not(a > 0))        # Output: False


# Bitwise Operators in Python

print("Bitwise AND:", a & b)  # Output: 0
print("Bitwise OR:", a | b)   # Output: 15
print("Bitwise XOR:", a ^ b)  # Output: 15
print("Bitwise NOT:", ~a)     # Output: -11

# Shift Operators in Python

print("Left Shift:", a << 1)  # Output: 20
print("Right Shift:", a >> 1) # Output: 5


# Assignment Operators in Python

c = 10

c += 5  # c = c + 5
print("c after += 5:", c)  # Output: 15

c -= 3  # c = c - 3
print("c after -= 3:", c)  # Output: 12

c *= 2  # c = c * 2
print("c after *= 2:", c)  # Output: 24

c /= 4  # c = c / 4
print("c after /= 4:", c)  # Output: 6.0

c //= 2  # c = c // 2
print("c after //= 2:", c)  # Output: 3.0

c %= 2  # c = c % 2
print("c after %= 2:", c)  # Output: 1.0

